/**
 * @extends ServiceEntityRepository<Evenement>
 *
 * @method Evenement|null find($id, $lockMode = null, $lockVersion = null)
 * @method Evenement|null findOneBy(array $criteria, array $orderBy = null)
 * @method Evenement[]    findAll()
 * @method Evenement[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class EvenementRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Evenement::class);
    }

    public function save(Evenement $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Evenement $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }
    public function getSponsorByEvenementId(EntityManagerInterface $entityManager, int $idEvent): array
    {
        $queryBuilder = $entityManager->createQueryBuilder();

        $queryBuilder->select('s')
            ->from('App\Entity\Sponsor', 's')
            ->join('App\Entity\EvenementSponsor', 'r', 'WITH', 'r.ID_sponsor = s.idSponsor')
            ->join('App\Entity\Evenement', 'e', 'WITH', 'r.ID_event = e.idEvent')
            ->where('e.idEvent = :idEvent')
            ->setParameter('idEvent', $idEvent);

        return $queryBuilder->getQuery()->getResult();
    }

